import * as express from 'express';
import { generateToken } from '../controllers/authentication';
import { validate } from '../middleware/validate';
import { authenticationSchema } from '../validators/authenticationValidators';

const router = express.Router();

/**
 * @swagger
 * /token:
 *   post:
 *     summary: Generate authentication token for admin users
 *     description: Authenticates an admin user with email and password, and returns a JWT token if valid.
 *     tags:
 *       - Authentication
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *             properties:
 *               email:
 *                 type: string
 *                 format: email
 *                 example: admin@example.com
 *               password:
 *                 type: string
 *                 format: password
 *                 example: strongpassword123
 *     responses:
 *       200:
 *         description: Token generated successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 token:
 *                   type: string
 *                   example: eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9...
 *       401:
 *         description: Access denied due to invalid credentials or user not being an admin
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 error:
 *                   type: string
 *                   example: Access denied
 *       500:
 *         description: Server error while generating token
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 error:
 *                   type: string
 *                   example: Error generating token
 */
router.post('/token', validate(authenticationSchema), generateToken);

export default router